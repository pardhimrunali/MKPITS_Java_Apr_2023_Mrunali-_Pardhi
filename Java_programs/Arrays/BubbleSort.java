class BubbleSort
{
public static void main(String args[])
{
  int array[]={5,15,9,7,20};
  
  int outerloop;
  int innerloop;
  int temporary;
  
  
  for(outerloop=0;outerloop<4;outerloop++)
  {
  for(innerloop=0;innerloop<4-outerloop;innerloop++)
  {
  if(array[innerloop]>array[innerloop+1])
  {
	  temporary=array[innerloop];
	  array[innerloop]=array[innerloop+1];
	  array[innerloop+1]=temporary;
	  
  }
}
}
for(int count=0;count<array.length;count++)
{
	System.out.println(array[count]);
}
}}
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  