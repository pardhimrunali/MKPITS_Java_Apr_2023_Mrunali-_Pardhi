import java.util.PriorityQueue;

public class PriorityQueueMethodsExample {
    public static void main(String[] args)
    {
        PriorityQueue mark = new PriorityQueue(20);
        mark.add(78);
        mark.add(97);
        mark.add(63);
        mark.add(21);
        mark.add(64);
        mark.remove();

        System.out.println(mark);




    }
}
